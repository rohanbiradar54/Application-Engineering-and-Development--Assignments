public class EmployerDetails {
    
    private String StudentCredits;
    private String StudentGraduationFlag;
    private String StudentGraduationStatus; 
    private float  ProfessorRating;
    private String StudentName;
    

    public String getStudentCredits() {
        return StudentCredits;
    }

    public void setStudentCredits(String StudentCredits) {
        this.StudentCredits = StudentCredits;
    }

    public String getStudentGraduationFlag() {
        return StudentGraduationFlag;
    }

    public void setStudentGraduationFlag(String StudentGraduationFlag) {
        this.StudentGraduationFlag = StudentGraduationFlag;
    }

    public String getStudentGraduationStatus() {
        return StudentGraduationStatus;
    }

    public void setStudentGraduationStatus(String StudentGraduationStatus) {
        this.StudentGraduationStatus = StudentGraduationStatus;
    }

    public float getProfessorRating() {
        return ProfessorRating;
    }

    public void setProfessorRating(float ProfessorRating) {
        this.ProfessorRating = ProfessorRating;
    }   

    public String getStudentName() {
        return StudentName;
    }

    public void setStudentName(String StudentName) {
        this.StudentName = StudentName;
    }
    
    
}