    private void populateTable() {
//        throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
    

        DefaultTableModel dtm = (DefaultTableModel) tblProfessorSearched.getModel();
        dtm.setRowCount(0);
        for (CourseDetails prf : courseDetailsDirectory.getCourseList()) {
            Object[] row = new Object[4];
            row[0]= prf.getProfessorName();
            row[1]= prf.getCourseName();
            row[2]= prf.getCourseCode();
            row[3]= prf.getIntake();
            //row[4]= prf.getProfessorRating();
            dtm.addRow(row);    
        }
      
    }

    private void populateTable1(CourseDetails prf) {
       // throw new UnsupportedOperationException("Not supported yet."); // Generated from nbfs://nbhost/SystemFileSystem/Templates/Classes/Code/GeneratedMethodBody
    
    DefaultTableModel dtm = (DefaultTableModel) tblProfessorSearched.getModel();
    dtm.setRowCount(0);

    if (selectedProfessor != null) {
        Object[] row = new Object[5]; // Include the rating column
        row[0] = selectedProfessor.getProfessorName();
        row[1] = selectedProfessor.getCourseName();
        row[2] = selectedProfessor.getCourseCode();
        row[3] = selectedProfessor.getIntake();
        row[4] = selectedProfessor.getProfessorRating(); // Rating column
        dtm.addRow(row);
    }
}
