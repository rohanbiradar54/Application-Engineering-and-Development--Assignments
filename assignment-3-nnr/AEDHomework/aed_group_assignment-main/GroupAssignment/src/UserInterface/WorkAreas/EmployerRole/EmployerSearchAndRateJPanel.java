    private void btnSearchProfessorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchProfessorActionPerformed
    String professorName = txtSearchProfessorName.getText().trim();
    CourseDetails prf = courseDetailsDirectory.searchProfessorName(professorName);

    if (prf != null) {
        selectedProfessor = prf; // Store the selected professor
        populateTable1(prf); // Pass the professor to the populateTable method
    } else {
        JOptionPane.showMessageDialog(null, "Professor does not exist", "Information", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnSearchProfessorActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        if (selectedProfessor != null) {
        int rating = Integer.parseInt(txtRating.getText());
        selectedProfessor.setProfessorRating(rating);
        populateTable1(selectedProfessor); // Pass the updated professor to the populateTable method
    } else {
        JOptionPane.showMessageDialog(null, "Please search for a professor first.", "Information", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnSaveActionPerformed


